function show() {
        if (document.getElementById("mode").value == "dark") {
            document.body.style.background = 'black';
        }
        
        if (document.getElementById("mode").value == "pink") {
            document.body.style.background = 'pink';
        }
        
        if (document.getElementById("mode").value == "green") {
            document.body.style.background = 'green';
        }
        
        if (document.getElementById("mode").value == "purple") {
            document.body.style.background = 'purple';
        }
        
        if (document.getElementById("mode").value == "yellow") {
            document.body.style.background = 'yellow';
        }
    }